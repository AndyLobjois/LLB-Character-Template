namespace Cactuar
{
    public class Vector2f
    {
        public IBGCBLLKIHA CreateVector2f(int x, int y)
        {
            return new IBGCBLLKIHA(x, y);
        }

        public IBGCBLLKIHA CreateVector2f(HHBCPNCDNDH x, HHBCPNCDNDH y)
        {
            return new IBGCBLLKIHA(x, y);
        }

        public IBGCBLLKIHA MultiplyByFloatf(IBGCBLLKIHA v, HHBCPNCDNDH ff)
        {
            return IBGCBLLKIHA.AJOCFFLIIIH(v, ff);
        }

        public IBGCBLLKIHA Zero()
        {
            return IBGCBLLKIHA.DBOMOJGKIFI;
        }

        public IBGCBLLKIHA Down()
        {
            return IBGCBLLKIHA.DEKDADEGAIK;
        }
    }
}
